package day17_While_DoWhile;

public class WhileLoopIntro {
    public static void main(String[] args) {

        if (true){
            System.out.println("Wooden Spoon");  // it will be written 1 time
        }
        System.out.println("---------------------------------------");
        // While Loop ==> is a repeated if statement
        while (true){
            System.out.println("Wooden Spoon");  // it will be written infinite
        }



    }
}
